pub struct OuiLookup {
    entries: HashMap<String, String>,
}

impl OuiLookup {
    pub fn new() -> Self {
        let mut entries = HashMap::new();
        for (prefix, vendor) in OUI_DB {
            entries.insert(prefix.to_uppercase(), vendor.to_string());
        }
        OuiLookup { entries }
    }

    pub fn lookup(&self, mac: &str) -> String {
        let normalized = mac.to_uppercase();
        let prefix: String = normalized.chars().take(8).collect();

        if let Some(vendor) = self.entries.get(&prefix) {
            return vendor.clone();
        }

        let prefix7: String = normalized.chars().take(8).collect();
        if let Some(vendor) = self.entries.get(&prefix7) {
            return vendor.clone();
        }

        "Unknown".to_string()
    }
}

static LOOKUP: std::sync::OnceLock<OuiLookup> = std::sync::OnceLock::new();

pub fn lookup_vendor(mac: &str) -> String {
    let lookup = LOOKUP.get_or_init(OuiLookup::new);
    lookup.lookup(mac)
}
